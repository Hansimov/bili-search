"""Search DSL syntax reference for the copilot system prompt.

Principles: simple, unified, clear, strict.
Only expose canonical forms — no aliases or syntax sugar.
"""

SEARCH_SYNTAX = """[SEARCH_SYNTAX]
搜索语句格式: `<关键词> <过滤器>`

## 一、关键词

关键词之间用空格分隔，表示同时搜索多个词。

| 语法 | 含义 | 示例 |
|------|------|------|
| `词` | 搜索包含该词的视频 | `黑神话` |
| `词1 词2` | 同时包含多个词 | `黑神话 悟空` |
| `"短语"` | 精确匹配完整短语 | `"黑神话 悟空"` |
| `+词` | 结果必须包含该词 | `游戏实况 +黑神话` |
| `-词` | 排除包含该词的结果 | `Python 教程 -广告` |

## 二、过滤器

过滤器以 `:` 起始，格式为 `:<字段><操作符><值>`。

### 1. 统计过滤器

字段: `view`(播放), `like`(点赞), `coin`(投币), `danmaku`(弹幕), `reply`(评论), `favorite`(收藏), `share`(分享)

数值单位: `k`=千, `w`=万, `m`=百万

| 语法 | 含义 |
|------|------|
| `:view>=1w` | 播放量 ≥ 1万 |
| `:coin>1k` | 投币 > 1千 |
| `:like<500` | 点赞 < 500 |
| `:view=[1w, 10w]` | 播放量在 1万 到 10万 之间（含边界） |
| `:view=(1k, 1w)` | 播放量在 1千 到 1万 之间（不含边界） |

### 2. 日期过滤器

字段: `date`

相对时间: `Nh`(N小时), `Nd`(N天), `Nw`(N周), `Nm`(N月), `Ny`(N年)

绝对日期: `YYYY`, `YYYY-MM`, `YYYY-MM-DD`

| 语法 | 含义 |
|------|------|
| `:date<=7d` | 7天内 |
| `:date<=3h` | 3小时内 |
| `:date>=2024-01` | 2024年1月之后 |
| `:date=2024` | 2024年内 |
| `:date=[2024-01, 2024-06]` | 2024年1月到6月之间 |

### 3. 视频时长过滤器

字段: `t`

时长格式: `Ns`(N秒), `Nm`(N分钟), `Nh`(N小时), 可组合如 `1h30m`

| 语法 | 含义 |
|------|------|
| `:t>5m` | 时长大于5分钟 |
| `:t<=30m` | 时长不超过30分钟 |
| `:t=[5m, 30m]` | 时长在5到30分钟之间 |

### 4. UP主昵称过滤器

字段: `user`

| 语法 | 含义 |
|------|------|
| `:user=影视飓风` | 指定UP主 |
| `:user=["影视飓风", "飓多多StormCrew"]` | 指定多个UP主（任一匹配） |
| `:user!=某UP主` | 排除该UP主 |

### 5. UP主UID过滤器

字段: `uid`

| 语法 | 含义 |
|------|------|
| `:uid=946974` | 指定UP主UID |
| `:uid=[946974, 1780480185]` | 指定多个UID |

### 6. 搜索模式

字段: `q`

| 值 | 含义 |
|------|------|
| `q=w` | 仅文字搜索 |
| `q=v` | 仅向量搜索 |
| `q=wv` | 混合搜索（默认） |

## 三、组合示例

- `黑神话 :view>=1w :date<=30d` → 搜索"黑神话"，播放≥1万，30天内
- `:user=影视飓风 :date<=7d` → 影视飓风最近7天的视频
- `Python 教程 -广告 :date<=1y :view>=1k` → Python教程，排除广告，1年内，播放≥1千
- `:user=["老番茄", "影视飓风"] :date<=30d` → 两个UP主最近30天的视频
- `游戏评测 +黑神话 :view>=5w` → 游戏评测且必含"黑神话"，播放≥5万
- `:user=何同学 :t>10m` → 何同学时长超过10分钟的视频

[/SEARCH_SYNTAX]"""
